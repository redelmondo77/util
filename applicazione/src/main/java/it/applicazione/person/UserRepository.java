package it.applicazione.person;

import java.util.List;

import org.springframework.cache.annotation.CacheEvict;
import org.springframework.cache.annotation.CachePut;
import org.springframework.cache.annotation.Cacheable;
import org.springframework.cache.annotation.Caching;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.Repository;
import org.springframework.data.repository.query.Param;
import org.springframework.transaction.annotation.Transactional;

/**
 * Repository class for <code>User</code> domain objects All method names are compliant with Spring Data naming
 * conventions so this interface can easily be extended for Spring Data See here: http://static.springsource.org/spring-data/jpa/docs/current/reference/html/jpa.repositories.html#jpa.query-methods.query-creation
 *
 * @author Ken Krebs
 * @author Juergen Hoeller
 * @author Sam Brannen
 * @author Michael Isvy
 */
public interface UserRepository extends Repository<User, Integer> {


    /**
     * Retrieve a {@link User} from the data store by id.
     * @param id the id to search for
     * @return the {@link User} if found
     */
	@Cacheable("usersCache")
	User findById(Long id);
	
	@Query("SELECT user FROM User user left join fetch user.roles WHERE user.username LIKE :username%")
	// @Transactional(readOnly = true)
	User findByUsername(@Param("username") String username);

    /**
     * Save a {@link User} to the data store, either inserting or updating it.
     * @param user the {@link User} to save
     */

	User save(User user);


}

